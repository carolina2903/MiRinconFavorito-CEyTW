<h3>Medios</h3>

<p style="font-family:courier;" style="font-size:300%;">
Al subir imágenes a la web deberías evitar subirlas con datos de ubicación (GPS EXIF) incluidos. Los visitantes de la web pueden descargar y extraer cualquier dato de localización de las imágenes de la web.
</p>

<h3>Cookies</h3>

<p style="font-family:courier;" style="font-size:300%;">
Si dejas un comentario en nuestro sitio puedes elegir guardar tu nombre, dirección de correo electrónico y web en cookies. Esto es para tu comodidad, para que no tengas que volver a rellenar tus datos cuando dejes otro comentario. Estas cookies tendrán una duración de un año.
<br>
Si tienes una cuenta y te conectas a este sitio, instalaremos una cookie temporal para determinar si tu navegador acepta cookies. Esta cookie no contiene datos personales y se elimina al cerrar el navegador.
<br>
Cuando inicias sesión, también instalaremos varias cookies para guardar tu información de inicio de sesión y tus opciones de visualización de pantalla. Las cookies de inicio de sesión duran dos días, y las cookies de opciones de pantalla duran un año. Al seleccionar «Recordarme», tu inicio de sesión perdurará durante dos semanas. Cuando sales de tu cuenta, las cookies de inicio de sesión se eliminarán.
<br>
Si editas o publicas un artículo se guardará una cookie adicional en tu navegador. Esta cookie no incluye datos personales y simplemente indica el ID del artículo que acabas de editar. Caduca después de 1 día.
</p>


