<h1>Política de cambios y devoluciones</h1>


<p style="font-family:courier;" style="font-size:300%;">
En Mi Rincón Favorito realizamos productos personalizados, hechos a medida para cada uno de vosotros. Por tanto, nos resulta imposible revenderlos en caso de devolución. Nuestra política de cambios y devoluciones dice que no podemos devolver el dinero salvo en casos excepcionales que explicaremos más abajo. Lee bien toda la información y si tienes duda ponte en contacto con nosotros.
</p>

<p style="font-family:courier;" style="font-size:300%;">
Según la <a href="https://www.boe.es/buscar/act.php?id=BOE-A-2007-20555">Ley General para la Defensa de los Consumidores y Usuarios, aprobada por el Real Decreto Legislativo 1/2007, de 16 de noviembre:</a>
<br>
<b>Artículo 103. Excepciones al derecho de desistimiento:</b>
<br>
El derecho de desistimiento no será aplicable a los contratos que se refieran a:
<br>
<i>c) El suministro de bienes confeccionados conforme a las especificaciones del consumidor y usuario o claramente personalizados.</i>
</p>
<p style="font-family:courier;" style="font-size:300%;">
Es por eso que no se admiten devoluciones de los productos salvo que te encuentres en alguno de los siguientes casos:
</p>

<ol style="font-family:courier;" style="font-size:300%;">
    <li>No has recibido el producto que habías encargado, o el que has recibido es muy distinto a lo que debería ser. </li>
    <li>El producto es equivocado y el fallo ha sido nuestro.</li>
    <li>El producto está claramente deteriorado.</li>
</ol>
<p style="font-family:courier;" style="font-size:300%;" style="magin-left: .4em">

En caso de encontrarte en alguno de los casos mencionados, ponte en contacto con nosotros a través de nuestro email, teléfono o facebook. Resolveremos la incidencia lo más rápido que podamos. 
</p>

<p style="font-family:courier;" style="font-size:300%;">
Para cualquier inconveniente con nuestra política de cambios y devoluciones, o en el caso de que te encuentres en alguna de las tres situaciones explicadas anteriormente utiliza este enlace para comunicárnoslo: contacta con nosotros.
<br>
Agradecemos tu comprensión ante este tema.
</p>

